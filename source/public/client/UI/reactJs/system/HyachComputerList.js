import React, { Component } from 'react';
import styled from 'styled-components';

const Container = styled.div`
    display: flex;
    flex-direction: column;
    margin-top: 5px;
    width: 100%;
    min-width: 200px;
    opacity: 0.95;
    background-color: rgba(32, 0, 32, 0.9);
    border: 1px solid #5d3564;
`;

const Header = styled.div`
    padding: 3px;
    background-color: #5d3564;
    border: 1px solid #5d3564;
    border-bottom: 1px solid #5d3564;    
    color: #f2f2f2;
    text-align: center;
    font-size: 11px;
    margin-bottom: 2px;
    opacity: 1 !important;     
    font-weight: bold;
`;

const ListContainer = styled.div`
    max-height: 200px;
    overflow-y: auto;
    display: block;
    padding: 0;
`;

const Row = styled.div`
    display: flex;
    align-items: center;
    padding: 3px 5px;
    border-bottom: 1px solid #5d3564;
    font-size: 11px;
    color: #f2f2f2;

    &:hover {
        background-color: rgba(75, 43, 81, 0.6);
    }
`;

const Icon = styled.img`
    width: 20px;
    height: 20px;
    margin-right: 8px;
`;

const Name = styled.div`
    flex: 1;
    font-weight: normal; 
`;

const Controls = styled.div`
    display: flex;
    align-items: center;
    gap: 2px;
`;

const Value = styled.div`
    width: 20px;
    text-align: center;
`;

const ActionButton = styled.div`
    width: 16px;
    height: 16px;
    background: #5d3564;
    border: 1px solid #7c4686;
    color: #f2f2f2;
    cursor: pointer;
    display: flex;
    justify-content: center;
    align-items: center;
    font-size: 14px;
    padding: 0;
    opacity: 0.9;

    &:hover {
        background: #5e3666;
        color: #ffffff;
        opacity: 1;
    }

    ${props => props.disabled && `
        opacity: 0.3;
        cursor: not-allowed;
        &:hover { background: #4b2b51; color: #d8b9e6; }
    `}
`;

class HyachComputerList extends Component {
    constructor(props) {
        super(props);
        this.listRef = React.createRef();
    }

    handleIncrease(className) {
        const { system } = this.props;
        system.setCurrFCType(className);

        if (system.canIncrease()) {
            system.doIncrease();
            this.forceUpdate();
        }
    }

    handleDecrease(className) {
        const { system } = this.props;
        system.setCurrFCType(className);

        if (system.canDecrease()) {
            system.doDecrease();
            this.forceUpdate();
        }
    }

    handlePropagate(className) {
        const { ship, system } = this.props;

        const gamedata = window.gamedata;
        const shipManager = window.shipManager;
        const webglScene = window.webglScene;

        console.log("Propagating BFCP setting for:", className);

        system.setCurrFCType(className);
        const allocated = system.allocatedBFCP[className];

        var allOwnBFCP = [];
        for (var i in gamedata.ships) {
            var otherUnit = gamedata.ships[i];
            if (otherUnit.userid != ship.userid) continue;
            if (shipManager.isDestroyed(otherUnit)) continue;

            if (otherUnit.flight) {
                for (var iFtr = 0; iFtr < otherUnit.systems.length; iFtr++) {
                    var ftr = otherUnit.systems[iFtr];
                    if (ftr) for (var iSys = 0; iSys < ftr.systems.length; iSys++) {
                        var ctrl = ftr.systems[iSys];
                        if (ctrl) if (ctrl.displayName == "Computer") {
                            allOwnBFCP.push(ctrl);
                            break;
                        }
                    }
                }
            } else {
                for (var iSys = 0; iSys < otherUnit.systems.length; iSys++) {
                    var ctrl = otherUnit.systems[iSys];
                    if (ctrl.displayName == "Computer") {
                        allOwnBFCP.push(ctrl);
                        break;
                    }
                }
            }
        }

        console.log("Found BFCP controllers:", allOwnBFCP.length);

        for (var c = 0; c < allOwnBFCP.length; c++) {
            var ctrl = allOwnBFCP[c];
            ctrl.setCurrFCType(className);

            let safety = 0;
            while (
                ctrl.getCurrAllocated() < allocated
                && ctrl.canIncrease()
                && safety < 100
            ) {
                ctrl.doIncrease();
                safety++;
            }
            if (safety >= 100) console.warn("BFCP Propagation safety break for", ctrl);
        }

        webglScene.customEvent('SystemDataChanged', { ship: ship, system: system });
    }

    render() {
        const { system } = this.props;

        if (!system || !system.allocatedBFCP) return null;

        const fcTypes = Object.keys(system.allocatedBFCP);
        const allocatedBFCP = system.allocatedBFCP;

        const canIncrease = (type) => {
            // Replicating HyachComputer.prototype.canIncrease logic locally for UI feedback
            const totalUsed = system.BFCPtotal_used;
            const totalMax = system.output;
            const allocated = allocatedBFCP[type] || 0;
            const perTypeMax = system.BFCPpertype;

            if (totalUsed >= totalMax) return false;
            if (allocated >= perTypeMax) return false;

            return true;
        };

        const canDecrease = (type) => {
            return (allocatedBFCP[type] > 0);
        };

        const canPropagate = (type) => {
            return (allocatedBFCP[type] > 0);
        };

        return (
            <Container>
                <Header>
                    Hyach Computer
                </Header>
                <ListContainer ref={this.listRef}>
                    {fcTypes.map(type => (
                        <Row key={type}>
                            <Icon src={`./img/systemicons/BFCPclasses/${type}.png`} alt={type} />
                            <Name>{type}</Name>
                            <Controls>
                                <ActionButton
                                    onClick={() => this.handleDecrease(type)}
                                    disabled={!canDecrease(type)}
                                >
                                    -
                                </ActionButton>
                                <Value>{allocatedBFCP[type]}</Value>
                                <ActionButton
                                    onClick={() => this.handleIncrease(type)}
                                    disabled={!canIncrease(type)}
                                >
                                    +
                                </ActionButton>
                                <ActionButton
                                    title="Propagate to all units"
                                    onClick={() => this.handlePropagate(type)}
                                    disabled={!canPropagate(type)}
                                    style={{ marginLeft: '5px' }}
                                >
                                    <img src="./img/systemicons/BFCPclasses/iconPropagate.png" alt="Propagate" style={{ width: '12px', height: '12px' }} />
                                </ActionButton>
                            </Controls>
                        </Row>
                    ))}
                    {fcTypes.length === 0 && <Row>No FC types available</Row>}
                </ListContainer>
                <div style={{ padding: '5px', textAlign: 'center', fontSize: '10px', color: '#f2f2f2' }}>
                    Total: {system.BFCPtotal_used} / {system.output} | Max Per Type: {system.BFCPpertype}
                </div>
            </Container>
        );
    }
}

export default HyachComputerList;
